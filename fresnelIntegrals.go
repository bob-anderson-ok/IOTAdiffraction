func polevl(x float64, coeff []float64) float64 {
	out := 0.0
	for _, c := range coeff {
		out = out*x + c
	}
	return out
}

func p1evl(x float64, coeff []float64) float64 {
	out := x + coeff[0]
	for _, c := range coeff[1:] {
		out = out*x + c
	}
	return out
}

func fresnelCephesScalar(x float64) (float64, float64) {
	sign := 1.0
	if x < 0 {
		x = -x
		sign = -1.0
	}

	x2 := x * x
	if x2 < 2.5625 {
		t := x2 * x2
		S := x * x2 * polevl(t, SN) / p1evl(t, SD)
		C := x * polevl(t, CN) / polevl(t, CD)
		return sign * S, sign * C
	}

	if x > 36974.0 {
		// http: //functions.wolfram.com/GammaBetaErf/FresnelC/06/02/
		// http: //functions.wolfram.com/GammaBetaErf/FresnelS/06/02/
		C := 0.5 + 1.0/(math.Pi*x)*math.Sin(math.Pi*x*x/2.0)
		S := 0.5 - 1.0/(math.Pi*x)*math.Cos(math.Pi*x*x/2.0)
		return sign * S, sign * C
	}

	// Asymptotic
	t := math.Pi * x2
	u := 1.0 / (t * t)
	t = 1.0 / t

	f := 1.0 - u*polevl(u, FN)/p1evl(u, FD)
	g := t * polevl(u, GN) / p1evl(u, GD)

	t = math.Pi * x2 / 2.0

	c := math.Cos(t)
	s := math.Sin(t)
	t = math.Pi * x

	C := 0.5 + (f*s-g*c)/t
	S := 0.5 - (f*c+g*s)/t

	return sign * S, sign * C
}
